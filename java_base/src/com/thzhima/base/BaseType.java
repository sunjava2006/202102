package com.thzhima.base;

public class BaseType {

	public static void main(String[] args) {
		
		byte a = -128;  //8位
		
		short s = 32767;  // 16位
		
		int i = 2147483647; // 32 位
		
		long l = 9223372036854775807L; // 64 位
		
		float f = 3.14F; // 32位

		double d = 3.14; // 64位
		
		char c = 'a'; // 用''只能有一个字符。unicode编码。
		
		boolean b = true;
		boolean b2 = false;
//		boolean b3 = 0; 任何类型都不可以转换为boolean 类型。
		
		String str = "a"; // 字符串，是对象类型。用""。
		
	}
	
}
